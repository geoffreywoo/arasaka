function updateTelemetry() {
  const seed = Date.now() / 1000;
  const latency = 2.4 + ((Math.sin(seed / 3) + 1) * 0.9);
  const pulse = 97.2 + ((Math.cos(seed / 5) + 1) * 1.1);
  const cipher = Math.abs(Math.sin(seed / 7) * 0xffffff)
    .toString(16)
    .slice(0, 6)
    .padStart(6, "0")
    .toUpperCase();

  telemetryNodes.forEach((node) => {
    if (node.dataset.telemetry === "latency") {
      node.textContent = `${latency.toFixed(2)}ms`;
    }
    if (node.dataset.telemetry === "cipher") {
      node.textContent = `0x${cipher}`;
    }
    if (node.dataset.telemetry === "pulse") {
      node.textContent = `${pulse.toFixed(2)}%`;
    }
  });
}

