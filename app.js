function initHeroCanvas() {
  if (!heroCanvas) return;

  const context = heroCanvas.getContext("2d");
  const frame = heroCanvas.closest(".hero");
  const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  const nodes = [
    { x: 0.08, y: 0.68, r: 2.4, type: "ghost" },
    { x: 0.18, y: 0.42, r: 3.2, type: "capital" },
    { x: 0.31, y: 0.58, r: 2.8, type: "neural" },
    { x: 0.47, y: 0.36, r: 3.8, type: "security" },
    { x: 0.58, y: 0.62, r: 2.8, type: "capital" },
    { x: 0.69, y: 0.28, r: 5.8, type: "core" },
    { x: 0.76, y: 0.49, r: 4.4, type: "neural" },
    { x: 0.84, y: 0.2, r: 2.6, type: "security" },
    { x: 0.9, y: 0.62, r: 3.4, type: "ghost" },
    { x: 0.96, y: 0.38, r: 2.2, type: "capital" }
  ];
  const links = [
    [0, 1], [1, 2], [2, 3], [3, 5], [3, 4], [4, 6], [5, 6],
    [5, 7], [6, 8], [7, 9], [8, 9], [2, 6], [1, 5]
  ];
  const colors = {
    core: "182, 243, 91",
    neural: "116, 199, 216",
    security: "225, 7, 20",
    capital: "209, 177, 107",
    ghost: "243, 244, 242"
  };

  function resize() {
    const rect = frame.getBoundingClientRect();
    const pixelRatio = window.devicePixelRatio || 1;
    heroCanvas.width = Math.max(1, Math.floor(rect.width * pixelRatio));
    heroCanvas.height = Math.max(1, Math.floor(rect.height * pixelRatio));
    heroCanvas.style.width = `${rect.width}px`;
    heroCanvas.style.height = `${rect.height}px`;
    context.setTransform(pixelRatio, 0, 0, pixelRatio, 0, 0);
  }

  function draw(timestamp = 0) {
    const width = heroCanvas.clientWidth;
    const height = heroCanvas.clientHeight;
    const time = prefersReducedMotion ? 1800 : timestamp;
    context.clearRect(0, 0, width, height);

    context.lineWidth = 1;
    for (let index = 0; index < 9; index += 1) {
      const x = width * (0.46 + index * 0.065);
      const drift = Math.sin(time / 2200 + index) * 18;
      context.strokeStyle = `rgba(116, 199, 216, ${0.035 + index * 0.005})`;
      context.beginPath();
      context.moveTo(x + drift, height * 0.08);
      context.lineTo(x - width * 0.22 + drift, height * 0.92);
      context.stroke();
    }

    const coreX = width * 0.72;
    const coreY = height * 0.43;
    for (let ring = 0; ring < 4; ring += 1) {
      const radius = 62 + ring * 38 + Math.sin(time / 900 + ring) * 4;
      context.strokeStyle = `rgba(${ring % 2 ? colors.neural : colors.core}, ${0.16 - ring * 0.024})`;
      context.beginPath();
      for (let side = 0; side <= 6; side += 1) {
        const angle = -Math.PI / 2 + side * (Math.PI / 3) + time / (14000 + ring * 3000);
        const x = coreX + Math.cos(angle) * radius;
        const y = coreY + Math.sin(angle) * radius;
        if (side === 0) {
          context.moveTo(x, y);
        } else {
          context.lineTo(x, y);
        }
      }
      context.stroke();
    }

    links.forEach(([a, b], index) => {
      const start = nodes[a];
      const end = nodes[b];
      const pulse = (Math.sin(time / 520 + index * 0.7) + 1) / 2;
      const startColor = colors[start.type];
      const endColor = colors[end.type];
      const gradient = context.createLinearGradient(start.x * width, start.y * height, end.x * width, end.y * height);
      gradient.addColorStop(0, `rgba(${startColor}, ${0.12 + pulse * 0.14})`);
      gradient.addColorStop(1, `rgba(${endColor}, ${0.1 + (1 - pulse) * 0.16})`);
      context.strokeStyle = gradient;
      context.lineWidth = index % 4 === 0 ? 1.8 : 1;
      context.beginPath();
      context.moveTo(start.x * width, start.y * height);
      context.lineTo(end.x * width, end.y * height);
      context.stroke();

      const packet = (time / 2600 + index * 0.17) % 1;
      context.fillStyle = `rgba(${index % 2 ? colors.core : colors.security}, 0.82)`;
      context.beginPath();
      context.arc(
        (start.x + (end.x - start.x) * packet) * width,
        (start.y + (end.y - start.y) * packet) * height,
        2,
        0,
        Math.PI * 2
      );
      context.fill();
    });

    nodes.forEach((node, index) => {
      const x = node.x * width;
      const y = node.y * height;
      const color = colors[node.type];
      const pulse = (Math.sin(time / 620 + index) + 1) / 2;
      context.strokeStyle = `rgba(${color}, ${0.12 + pulse * 0.18})`;
      context.beginPath();
      context.arc(x, y, node.r * 7 + pulse * 12, 0, Math.PI * 2);
      context.stroke();
      context.fillStyle = `rgba(${color}, ${0.58 + pulse * 0.28})`;
      context.beginPath();
      context.arc(x, y, node.r, 0, Math.PI * 2);
      context.fill();
    });

    context.font = "700 11px SFMono-Regular, Consolas, monospace";
    context.fillStyle = "rgba(182, 243, 91, 0.28)";
    context.fillText("MIKOSHI PUBLIC EDGE // AA-90", width * 0.64, height * 0.74);

    if (!prefersReducedMotion) {
      requestAnimationFrame(draw);
    }
  }

  resize();
  draw();
  window.addEventListener("resize", () => {
    resize();
    if (prefersReducedMotion) draw();
  });
}

function initTopologyCanvas() {
  if (!topologyCanvas) return;

  const context = topologyCanvas.getContext("2d");
  const frame = topologyCanvas.closest(".topology-frame");
  const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  const nodes = [
    { x: 0.18, y: 0.22, r: 5, type: "core" },
    { x: 0.36, y: 0.18, r: 3, type: "asset" },
    { x: 0.62, y: 0.16, r: 4, type: "asset" },
    { x: 0.78, y: 0.28, r: 3, type: "risk" },
    { x: 0.28, y: 0.48, r: 4, type: "asset" },
    { x: 0.52, y: 0.42, r: 7, type: "core" },
    { x: 0.72, y: 0.5, r: 4, type: "risk" },
    { x: 0.16, y: 0.74, r: 3, type: "risk" },
    { x: 0.42, y: 0.76, r: 4, type: "asset" },
    { x: 0.68, y: 0.78, r: 3, type: "asset" },
    { x: 0.86, y: 0.7, r: 5, type: "core" }
  ];
  const links = [
    [0, 1], [1, 2], [2, 3], [0, 4], [4, 5], [5, 2], [5, 6],
    [6, 10], [4, 7], [7, 8], [8, 5], [8, 9], [9, 10], [3, 6]
  ];

  function resize() {
    const rect = frame.getBoundingClientRect();
    const pixelRatio = window.devicePixelRatio || 1;
    topologyCanvas.width = Math.max(1, Math.floor(rect.width * pixelRatio));
    topologyCanvas.height = Math.max(1, Math.floor(rect.height * pixelRatio));
    topologyCanvas.style.width = `${rect.width}px`;
    topologyCanvas.style.height = `${rect.height}px`;
    context.setTransform(pixelRatio, 0, 0, pixelRatio, 0, 0);
  }

  function draw(timestamp = 0) {
    const width = topologyCanvas.clientWidth;
    const height = topologyCanvas.clientHeight;
    const time = prefersReducedMotion ? 1200 : timestamp;
    context.clearRect(0, 0, width, height);

    context.fillStyle = "#06080a";
    context.fillRect(0, 0, width, height);

    context.strokeStyle = "rgba(116, 199, 216, 0.08)";
    context.lineWidth = 1;
    for (let x = 0; x < width; x += 42) {
      context.beginPath();
      context.moveTo(x, 0);
      context.lineTo(x, height);
      context.stroke();
    }
    for (let y = 0; y < height; y += 42) {
      context.beginPath();
      context.moveTo(0, y);
      context.lineTo(width, y);
      context.stroke();
    }

    links.forEach(([a, b], index) => {
      const start = nodes[a];
      const end = nodes[b];
      const pulse = (Math.sin(time / 520 + index * 0.9) + 1) / 2;
      const gradient = context.createLinearGradient(start.x * width, start.y * height, end.x * width, end.y * height);
      gradient.addColorStop(0, `rgba(116, 199, 216, ${0.18 + pulse * 0.18})`);
      gradient.addColorStop(0.55, `rgba(182, 243, 91, ${0.08 + pulse * 0.16})`);
      gradient.addColorStop(1, `rgba(225, 7, 20, ${0.16 + (1 - pulse) * 0.16})`);
      context.strokeStyle = gradient;
      context.lineWidth = index % 4 === 0 ? 2 : 1;
      context.beginPath();
      context.moveTo(start.x * width, start.y * height);
      context.lineTo(end.x * width, end.y * height);
      context.stroke();

      const packet = (time / 2400 + index * 0.13) % 1;
      context.fillStyle = index % 3 === 0 ? "rgba(225, 7, 20, 0.92)" : "rgba(182, 243, 91, 0.86)";
      context.beginPath();
      context.arc(
        (start.x + (end.x - start.x) * packet) * width,
        (start.y + (end.y - start.y) * packet) * height,
        2.2,
        0,
        Math.PI * 2
      );
      context.fill();
    });

    nodes.forEach((node, index) => {
      const x = node.x * width;
      const y = node.y * height;
      const pulse = (Math.sin(time / 680 + index) + 1) / 2;
      const color = node.type === "risk" ? "225, 7, 20" : node.type === "core" ? "182, 243, 91" : "116, 199, 216";

      context.strokeStyle = `rgba(${color}, ${0.22 + pulse * 0.24})`;
      context.lineWidth = 1;
      context.beginPath();
      context.arc(x, y, node.r * 6 + pulse * 10, 0, Math.PI * 2);
      context.stroke();

      context.fillStyle = `rgba(${color}, 0.95)`;
      context.beginPath();
      context.arc(x, y, node.r, 0, Math.PI * 2);
      context.fill();
    });

    if (!prefersReducedMotion) {
      requestAnimationFrame(draw);
    }
  }

  resize();
  draw();
  window.addEventListener("resize", () => {
    resize();
    if (prefersReducedMotion) draw();
  });
}

