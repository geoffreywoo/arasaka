function updateTerminal(forceLine) {
  if (!terminalLines.length) return;
  const language = document.documentElement.dataset.language || "en";
  const dictionary = translations[language] || translations.en;
  const lines = dictionary.terminal.lines;

  terminalLines.forEach((node, index) => {
    const line = forceLine && index === 0
      ? forceLine
      : lines[(terminalOffset + index) % lines.length];
    node.textContent = `> ${line}`;
  });

  if (terminalEntropy) {
    const entropy = Math.floor(72 + ((Math.sin(Date.now() / 1400) + 1) * 11));
    terminalEntropy.textContent = `${entropy}%`;
  }

  if (!forceLine) {
    terminalOffset = (terminalOffset + 1) % lines.length;
  }
}

