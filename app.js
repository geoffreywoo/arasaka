function initCursorReticle() {
  if (!cursorReticle) return;
  const finePointer = window.matchMedia("(pointer: fine)");
  const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)");

  function setEnabled() {
    const enabled = finePointer.matches && !reducedMotion.matches;
    cursorReticle.hidden = !enabled;
    cursorReticle.classList.toggle("is-visible", enabled);
  }

  window.addEventListener("pointermove", (event) => {
    if (cursorReticle.hidden) return;
    document.documentElement.style.setProperty("--cursor-x", `${event.clientX}px`);
    document.documentElement.style.setProperty("--cursor-y", `${event.clientY}px`);
  }, { passive: true });

  finePointer.addEventListener("change", setEnabled);
  reducedMotion.addEventListener("change", setEnabled);
  setEnabled();
}

function initSignalWeather() {
  if (!signalWeatherCanvas) return;

  const context = signalWeatherCanvas.getContext("2d");
  const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)");
  const glyphs = {
    en: "ARASAKA//BLACKLEDGER//MIKOSHI//ICE//RELIC//ORBIT//DRONE//",
    ja: "荒坂株式会社神経保管黒氷台帳軌道記憶防衛資本中継"
  };
  const streams = [];
  let width = 0;
  let height = 0;

  function rebuildStreams() {
    streams.length = 0;
    const count = Math.max(18, Math.floor(width / 42));
    for (let index = 0; index < count; index += 1) {
      streams.push({
        x: (index + 0.35 + Math.random() * 0.5) * (width / count),
        y: Math.random() * height,
        speed: 9 + Math.random() * 22,
        length: 5 + Math.floor(Math.random() * 12),
        phase: Math.floor(Math.random() * 19),
        hue: index % 5
      });
    }
  }

  function resize() {
    const pixelRatio = window.devicePixelRatio || 1;
    width = window.innerWidth;
    height = window.innerHeight;
    signalWeatherCanvas.width = Math.max(1, Math.floor(width * pixelRatio));
    signalWeatherCanvas.height = Math.max(1, Math.floor(height * pixelRatio));
    signalWeatherCanvas.style.width = `${width}px`;
    signalWeatherCanvas.style.height = `${height}px`;
    context.setTransform(pixelRatio, 0, 0, pixelRatio, 0, 0);
    rebuildStreams();
  }

  function draw(timestamp = 0) {
    const language = document.documentElement.dataset.language === "ja" ? "ja" : "en";
    const alphabet = glyphs[language];
    const time = reducedMotion.matches ? 2200 : timestamp;

    context.clearRect(0, 0, width, height);
    context.font = language === "ja"
      ? "12px 'Hiragino Kaku Gothic ProN', 'Yu Gothic', Meiryo, monospace"
      : "11px 'SFMono-Regular', Consolas, 'Liberation Mono', monospace";
    context.textAlign = "center";
    context.textBaseline = "middle";

    for (let x = 0; x < width; x += 96) {
      const pulse = (Math.sin(time / 1800 + x * 0.01) + 1) / 2;
      context.strokeStyle = `rgba(116, 199, 216, ${0.018 + pulse * 0.025})`;
      context.beginPath();
      context.moveTo(x + Math.sin(time / 2400 + x) * 18, 0);
      context.lineTo(x - width * 0.16, height);
      context.stroke();
    }

    streams.forEach((stream, streamIndex) => {
      for (let glyphIndex = 0; glyphIndex < stream.length; glyphIndex += 1) {
        const y = (stream.y + glyphIndex * 22 + time * 0.001 * stream.speed) % (height + 80) - 40;
        const alpha = Math.max(0, 1 - glyphIndex / stream.length);
        const glyph = alphabet[(stream.phase + glyphIndex + Math.floor(time / 260) + streamIndex) % alphabet.length];
        const color = stream.hue === 0
          ? `rgba(225, 7, 20, ${0.08 + alpha * 0.32})`
          : stream.hue === 1
            ? `rgba(182, 243, 91, ${0.06 + alpha * 0.28})`
            : `rgba(116, 199, 216, ${0.05 + alpha * 0.22})`;
        context.fillStyle = color;
        context.fillText(glyph, stream.x + Math.sin(time / 1000 + glyphIndex) * 4, y);
      }
    });

    const scanY = (time * 0.035) % (height + 180) - 90;
    const gradient = context.createLinearGradient(0, scanY - 80, width, scanY + 80);
    gradient.addColorStop(0, "rgba(225, 7, 20, 0)");
    gradient.addColorStop(0.5, "rgba(182, 243, 91, 0.12)");
    gradient.addColorStop(1, "rgba(116, 199, 216, 0)");
    context.fillStyle = gradient;
    context.fillRect(0, scanY - 80, width, 160);

    if (!reducedMotion.matches) {
      requestAnimationFrame(draw);
    }
  }

  resize();
  draw();
  window.addEventListener("resize", () => {
    resize();
    if (reducedMotion.matches) draw();
  });
  reducedMotion.addEventListener("change", () => draw());
}

function updateSignalSpine() {
  if (!spineMeter || !spineProgress) return;
  const scrollable = document.documentElement.scrollHeight - window.innerHeight;
  const progress = scrollable <= 0
    ? 0
    : Math.min(1, Math.max(0, window.scrollY / scrollable));
  const percent = Math.round(progress * 100).toString().padStart(2, "0");
  spineMeter.style.setProperty("--spine-progress", `${progress * 100}%`);
  spineProgress.textContent = `${percent}%`;
}

